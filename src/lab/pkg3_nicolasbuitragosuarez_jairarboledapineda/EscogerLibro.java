    private void tblEscogerLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEscogerLibroMouseClicked
        try {
            row = tblEscogerLibro.rowAtPoint(evt.getPoint());
            if (row >= 0) {
                if(sw){
                    dm.getLibro(row);
                }else{
                    dm.getDLibro(row);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al seleccionar el libro", "Error", 0);
        }
    }//GEN-LAST:event_tblEscogerLibroMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(dm.libro!=null){
                if(sw){
                    dm.setLibro();
                }else{
                    dm.setDLibro();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Primero debe seleccionar un libro.", "Error", 1);
            }
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al seleccionar el libro.", "Error", 0);
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

